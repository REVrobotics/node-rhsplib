export class AngularVelocity {
    xRotationRate: number;
    yRotationRate: number;
    zRotationRate: number;

    constructor(xRotationRate: number, yRotationRate: number, zRotationRate: number) {
        this.xRotationRate = xRotationRate;
        this.yRotationRate = yRotationRate;
        this.zRotationRate = zRotationRate;
    }
}
